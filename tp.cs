Exercice 1 :
Principaux rôles de Scrum :

Product Owner (Propriétaire du produit)
Scrum Master
Development Team (Équipe de développement)
Grandes étapes de la méthode Scrum :

Sprint Planning (Planification du sprint)
Sprint (Développement)
Daily Scrum (Réunion quotidienne)
Sprint Review (Revue du sprint)
Sprint Retrospective (Rétrospective du sprint)

User story : Une user story est une description simple d'une fonctionnalité ou d'une exigence du point de vue de l'utilisateur final.

Sprint : Un sprint est une période de développement fixe (généralement 2 à 4 semaines) durant laquelle l'équipe de développement travaille sur un ensemble spécifique de tâches.

Différence entre Product Backlog et Sprint Backlog :

Product Backlog : Liste priorisée des fonctionnalités, exigences, améliorations et corrections à apporter au produit.
Sprint Backlog : Liste des éléments du Product Backlog sélectionnés pour être réalisés durant un sprint.
Cycle de vie de la méthode Scrum : Un schéma devrait illustrer le cycle allant de la planification des sprints, à la revue des tâches et à l'amélioration continue via la rétrospective.







